let one = 1
let two = 2

// == Igual
console.log(one == 1)
console.log(one == "1")

// != Diferente 
console.log(one != two)
console.log(one != 1)
console.log(one != one)

// === Estritamente igual
console.log(one === "1")
console.log(one === 1)

// !== Estritamente diferente
console.log(one !== "1")

// > Maior
console.log(one > two)

// >= Maior ou igual
console.log(one >= 1)
console.log(two >= 1)

// < Menor
console.log(one < two)

// <= Menor ou igual
console.log(one <= two)