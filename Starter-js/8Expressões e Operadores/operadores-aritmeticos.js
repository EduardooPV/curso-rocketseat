// Operadores Aritiméticos

// Multiplicação = *
console.log(3.2 * 5.4)

// Divisão = /
console.log(12 / 2)

// Soma = +
console.log(10 + 2)

// Substração = -
console.log(10 - 5)

// Resto da Divisão = %
let remainder 
remainder = 11 % 9
console.log(remainder)

// Incremento = +P+
let increment = 0
increment++
console.log(increment)

// Decremento = --
let decrement = 0
decrement--
console.log(decrement)

// Exponencial = **
console.log(3 ** 3)
